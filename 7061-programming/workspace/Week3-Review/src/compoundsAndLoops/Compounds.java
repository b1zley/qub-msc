package compoundsAndLoops;

/**
 * looking at compound operators 
 */
public class Compounds {

	/**
	 * ++, -- , +=, -=,
	 * @param args
	 */
	public static void main(String[] args) {
		int num;
		num = 6;
		
		// instead of this
		
		num += 4;
		
		System.out.println(num);
		
		//increment operator
		num++;
		System.out.println(num);
		num--;

		System.out.println(num);
		
		
		
		
	}

}
